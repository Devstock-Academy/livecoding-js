'use strict'

const greeting = 'hello'
console.log(greeting.toUpperCase()) // Output: "HELLO"

const shout = 'HELLO'
console.log(shout.toLowerCase()) // Output: "hello"

const messyString = ' hello '
console.log(messyString.trim()) // Output: "hello"

const sentence = 'hello world'
console.log(sentence.split(' ')) // Output: ["hello", "world"]

const text = 'Hello World'
console.log(text.replace('World', 'Everyone')) // Output: "Hello Everyone"

console.log(text.indexOf('world')) // Output: 6
